print('Paragraph 2')
# створила обє"кти типу Order
p = Order('Борошно', 15, 10, weight_type='кг')
d = Order('Цукор', 25, 20, weight_type='кг')
r = Order('Рис', 30, 5, weight_type='кг')
print(p)
print(d)
print(r)
# застосувала функцію для отримання вартості замовлення
print(p.count_sum())
print(d.count_sum())
print(r.count_sum())
# застосувала перевикористаний оператор білше рівне для порівняння замовлень за вартістю
if p >= d:
    print(f'загальна вартість замовлення {p.name} більше рівна ніж у замовлення {d.name}')
else:
    print(f'загальна вартість замовлення {p.name} не більше рівна ніж у замовлення {d.name}')

if p >= r:
    print(f'загальна вартість замовлення {p.name} більше рівна ніж у замовлення {r.name}')
else:
    print(f'загальна вартість замовлення {p.name} не більше рівна ніж у замовлення {r.name}')
print('===================================')

