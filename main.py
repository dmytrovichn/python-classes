from product import Product
from orders import Order, Orders


print('Paragraph 1')
# створила обє"кт типу Product
p = Product('Борошно', 15)
# вивила у консолі інформацію про обєкт
print(p)
# застосувала  функцію для зміни системи вимірювання кг - г та вивила інформацю про обєкт
p.set_weight_type('г')
print(p)
# застосувала функцію для зміни системи вимірювання г - ц та вивила інформацю про обєкт
p.set_weight_type('ц')
print(p)
# застосувала функцію для зміни системи вимірювання ц - кг та вивила інформацю про обєкт
p.set_weight_type('кг')
print(p)
# застосувала перевикористаний оператор множення шкала відстоків від 1-100 та вивила інформацю про обєкт
p *= 100
print(p)
